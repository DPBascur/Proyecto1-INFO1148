public class ArithmeticExamples {
    public static void main(String[] args) {
        // Ejemplos de operaciones aritmeticas basicas
        int a = 10;
        int b = 5;
        
        // Suma
        int sum = a + b;
        
        // Resta
        int difference = a - b;
        
        // Multiplicacion
        int product = a * b;
        
        // Division
        int quotient = a / b;
        
        // Modulo
        int remainder = a % b;
        
        // Expresiones con numeros decimales
        double x = 3.14;
        double y = 2.71;
        double result = x + y;
        
        // Expresiones complejas con parentesis
        int complex1 = (a + b) * 2;
        int complex2 = a * (b + 3);
        int complex3 = (a + b) / (a - b);
        
        // Expresiones anidadas
        int nested = ((a + b) * (a - b)) / 2;
        
        // Multiples operadores
        int c = 1;
        int d = 2;
        int e = 3;
        int f = 4;
        int multi = a + b - c * d / e % f;
        
        // Expresiones con variables
        float pi = 3.14159;
        float radius = 5.0;
        float area = pi * radius * radius;
        float circumference = 2 * pi * radius;
        
        // Expresiones mas complejas
        int expr1 = a + b * c - d / e;
        int expr2 = (a + b) * (c + d);
        int expr3 = a * b + c * d - e * f;
    }
}
